WITH STEP_MAPPING AS (
    SELECT
        1 AS SORT_NO,
        'POL' AS CURRENT_AREANAME,
        'ME050' AS CURRENT_STEPID,
        'IQC/CLN' AS PREVIOUS_AREANAME,
        'ME030' AS PREVIOUS_STEPID
    FROM DUAL

    UNION ALL

    SELECT
        2,
        'TUFFY/CRD',
        'ME170',
        'T-FOF',
        'ME140'
    FROM DUAL

    UNION ALL

    SELECT
        3,
        'LAMI',
        'ME210',
        'TUFFY/CRD',
        'ME170'
    FROM DUAL

    UNION ALL

    SELECT
        4,
        'WFD',
        'ME250',
        'UV',
        'ME240'
    FROM DUAL
),

/* Products available at the current process in the last 4 hours */
CURRENT_PROCESS AS (
    SELECT
        M.SORT_NO,
        M.CURRENT_AREANAME,
        M.CURRENT_STEPID,
        M.PREVIOUS_AREANAME,
        M.PREVIOUS_STEPID,
        T.PRODUCTID,
        T.EQPID AS CURRENT_EQPID,
        MAX(T.RPTDATE) AS CURRENT_RPTDATE
    FROM STEP_MAPPING M
    JOIN YOUR_TABLE T
      ON T.AREANAME = M.CURRENT_AREANAME
     AND T.STEPID = M.CURRENT_STEPID
     AND T.RPTDATE >= SYSDATE - INTERVAL '4' HOUR
    GROUP BY
        M.SORT_NO,
        M.CURRENT_AREANAME,
        M.CURRENT_STEPID,
        M.PREVIOUS_AREANAME,
        M.PREVIOUS_STEPID,
        T.PRODUCTID,
        T.EQPID
),

/* TrackOut records at the previous process in the last 5 hours */
PREVIOUS_TRACKOUT AS (
    SELECT
        M.CURRENT_AREANAME,
        M.CURRENT_STEPID,
        T.PRODUCTID,
        T.EQPID AS PREVIOUS_EQPID,
        COUNT(*) AS TRACKOUT_COUNT,
        MAX(T.RPTDATE) AS LAST_TRACKOUT_RPTDATE
    FROM STEP_MAPPING M
    JOIN YOUR_TABLE T
      ON T.AREANAME = M.PREVIOUS_AREANAME
     AND T.STEPID = M.PREVIOUS_STEPID
     AND T.ACTIVITY = 'TrackOut'
     AND T.RPTDATE >= SYSDATE - INTERVAL '5' HOUR
    GROUP BY
        M.CURRENT_AREANAME,
        M.CURRENT_STEPID,
        T.PRODUCTID,
        T.EQPID
)

SELECT
    C.CURRENT_AREANAME,
    C.CURRENT_STEPID,
    C.PREVIOUS_AREANAME,
    C.PREVIOUS_STEPID,
    C.PRODUCTID,
    C.CURRENT_EQPID,
    P.PREVIOUS_EQPID,
    C.CURRENT_RPTDATE,
    P.LAST_TRACKOUT_RPTDATE,
    NVL(P.TRACKOUT_COUNT, 0) AS TRACKOUT_COUNT_LAST_5_HOURS,

    CASE
        WHEN NVL(P.TRACKOUT_COUNT, 0) = 0
        THEN 'NOT_OK'
        ELSE 'OK'
    END AS STATUS

FROM CURRENT_PROCESS C
LEFT JOIN PREVIOUS_TRACKOUT P
  ON P.CURRENT_AREANAME = C.CURRENT_AREANAME
 AND P.CURRENT_STEPID = C.CURRENT_STEPID
 AND P.PRODUCTID = C.PRODUCTID

ORDER BY
    C.SORT_NO,
    C.PRODUCTID,
    C.CURRENT_EQPID,
    P.PREVIOUS_EQPID;
